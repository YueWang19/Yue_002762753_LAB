    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String packageId = packageIdEdit.getText();
        String packageWeight = packageWeightEdit.getText();
        String customerId = customerIdEdit.getText();
        String customerName = customerFullNameEdit.getText();
        
        this.delPackage.setPackageId(Integer.valueOf(packageId));
        this.delPackage.setPackageWeight(Double.valueOf(packageWeight));
        
        Customer customer = this.delPackage.getCustomer();
        customer.setCustomerID(Integer.valueOf(customerId));
        customer.setFullName(customerName);
        
        if(validation){
            JOptionPane.showMessageDialog(null, "Please fill all the fields!");
        } else {
            JOptionPane.showMessageDialog(null, "Updated!");
        }
        
        
        
        
    }//GEN-LAST:event_updateBtnActionPerformed

    private void customerFullNameEditFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_customerFullNameEditFocusLost
        // TODO add your handling code here:
        System.out.println(customerFullNameEdit.getText() + " -- the customer name");
        String name = customerFullNameEdit.getText();
        
        if(name.isEmpty()){
            this.validation = true;
        } else {
            this.validation  = false;
            
        }
    }//GEN-LAST:event_customerFullNameEditFocusLost


    public void displayPackageInfo(){
        
        packageIdEdit.setText(String.valueOf(this.delPackage.getPackageId()));
        packageWeightEdit.setText(String.valueOf(this.delPackage.getPackageWeight()));
        
        Customer customer = this.delPackage.getCustomer();
        customerIdEdit.setText(String.valueOf(customer.getCustomerID()));
        customerFullNameEdit.setText(String.valueOf(customer.getFullName()));
        
    }
    
    public void displayProductList(){
        for(Product p : this.delPackage.getProductList()){
            productComboBox.addItem(p.getProductId());
        }
        
    }

