    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int pid = (int) productComboBox.getSelectedItem();
        // product id cannot be modify
        productIdEdit.setEnabled(false);
        //find the product in the arraylist
        Product product = this.delPackage.findProduct(pid);
        this.productShowed = product;
        
        if ( !product.equals(null)) { 
            productIdEdit.setText(String.valueOf(product.getProductId()));
            productNameEdit.setText(product.getProductName());
            productPriceEdit.setText(String.valueOf(product.getPrice()));    
        } else {
            JOptionPane.showMessageDialog(null,"Cannot find this product");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

