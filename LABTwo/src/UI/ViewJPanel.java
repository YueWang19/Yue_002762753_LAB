    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int pid = (int) productComboBoxInView.getSelectedItem();
        
        //find the product in the arraylist
        Product product = this.delPackage.findProduct(pid);
        this.productShowedInView= product;

        if ( !product.equals(null)) {
            productIdViewField.setText(String.valueOf(product.getProductId()));
            productNameViewField.setText(product.getProductName());
            productPriceViewField.setText(String.valueOf(product.getPrice()));
        } else {
            JOptionPane.showMessageDialog(null,"Cannot find this product");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


