    public void displayDetails() {
        int packageId = this.delPackage.getPackageId();
        packageIdView.setText(String.valueOf(packageId));
        
        double packageWeight = this.delPackage.getPackageWeight();
        packageWeightView.setText(String.valueOf(packageWeight));
        
        Customer customer = this.delPackage.getCustomer();
        customerFullNameView.setText(customer.getFullName());
        customerIdView.setText(String.valueOf(customer.getCustomerID()));
        

    }
    
      public void displayProductList(){
        for(Product p : this.delPackage.getProductList()){
            productComboBoxInView.addItem(p.getProductId());
        }
        
    }
    

